public class DataTypeDemo {
    public static void main(String[] args) {
        // Declare and assign different data types
        int age = 25;
        float height = 5.9f;     // 'f' is required for float literals
        double salary = 55000.75;
        char grade = 'A';
        boolean isEmployed = true;

        // Display the values
        System.out.println("Integer (int): " + age);
        System.out.println("Float (float): " + height);
        System.out.println("Double (double): " + salary);
        System.out.println("Character (char): " + grade);
        System.out.println("Boolean (boolean): " + isEmployed);
    }
}
